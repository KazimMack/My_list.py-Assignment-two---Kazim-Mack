# An empty list 
my_list = []

# Appended elements 
my_list.append(10)
my_list.append(20)
my_list.append(30)
my_list.append(40)

# Inserted value
my_list.insert(10, 15)

# Extended list
my_list.extend([50, 60, 70])

# Last element
my_list.pop()

# Sorted list
my_list.sort()

# index value of 30
index_of_30 = my_list.index(30)
print(f"index value of 30 in my_list is: {index_of_30}")
